import java.util.*;

public class whileloop {
    public static void main (String args[]){

        Scanner  sc = new Scanner(System.in);

        // WHILE LOOP K BASICS
        int counter = sc.nextInt();
        while(counter < 100){
            System.out.println(" RAJ ");
            counter ++;
        }
        System.out.println("RAJ is printed 100 time ");
    }
}